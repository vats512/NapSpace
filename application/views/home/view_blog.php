                                                    <p>
                                                    Maggi. The tangled structured food serving people, especially students since always. :D It is a crucial component in student’s life. </p>
                                                    <p>
No matter how your mood is, mostly people end up eating it.
“Hungry? Chal maggi khate hai yar”
“Sad? Chal, teko maggi khilata hu”
“In hurry ? Let’s have maggi quickly”
Even outside a PG, whenever one goes to a thadi,
“Bhaiya kya milega?” 
“Sir chai… aur maggi” :)
</p>
<p>

The easy-to-prepare 2 min noodles have become an integral part of not only youth but folks of all age groups. The joy of eating it at 3 in the morning is totally priceless. 

</p>
<p>
The un-eatable food served at hostels and PG’s have inspired students to prepare different forms of maggi. Be it “veg maggi”, “butter maggi”, “egg maggi” ,“masala maggi” and the list goes on… But even today, the original, classic flavor is unbeatable! 
<p>
Few reasons that helped Maggi reached this level of popularity among the PG students are:
</p><p>
<strong>Cheap </strong>- Available in market at Rs. 10/20 etc.</p><p>
<strong>Healthy </strong>- Makes your stomach quite full.</p><p>
<strong>Easy to cook </strong>- Anybody can prepare it, just need to boil and add the spices given with the pack of maggi.</p><p>
<strong>Easy availability </strong>- Almost all the departmental store has it.</p><p>
A quick power meal at a busy 2 AM, although the ten minutes of watching the boiling maggi in the pan would be the longest ten minutes one would encounter. And, that too very satisfying. Morning, you can sit in a bus and can slurp it quickly while commuting. Breakfast is done even when you are short of time. It is one dish guys can try without any fear to impress their significant ones. So, it is pretty much a life support for students residing in PG.
To sum up, Importance of spinach in popeye's life = Importance of maggi in student's life. ☺


Leave alone the use of maggi to fill empty stomachs, it has been more or less successful in sharing moments with your friends or roommates. The late-night maggi with deep conversations have created endless, cherishable memories in our hearts. 





