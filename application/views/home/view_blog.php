                                                    <p>You’ve had your head in books for years,  got the grades, got into university - good luck! If you’re moving out, here are some tips to pack, so the new place will not let you miss your home. 


First things first: travel light. Generally speaking, PG rooms aren’t very big – especially if you’re going into halls. Several people are usually accumulated together, so it’s best to avoid over-packing and causing clutter.
Before you leave home, check what’s already in your accommodation – sometimes you’ll need things like bins and desk lamps, sometimes you won’t. Call up your landlord/lady, if you can’t find the information online.
Here a few things you’d like to carry with you:
 Essentials
Stuff your bed: A duvet, two or three sets of bed sheets, duvet covers, and pillow cases. It’s worth getting these in doubles, even if you’re in a single bed.Getting additional pillows and blankets can give your bedroom a ‘homely’ feel.
Basic cleaning products – Even if you have a cleaner, it’s unlikely he or she will clean your room. Some Febreeze, Flash, washing-up liquid, and sponges will go a long way.
Ear plugs – Believe it or not, there will be nights where you want to keep while those who live near you want to party, so ear plugs are a must.
First-aid kit
Medicines - At Least the general ones including fever, cough, cold, painkillers etc.
Everyday things – Whatever you usually use; shower gel, soap, shampoo, conditioner, moisture, and toothpastes. As well as hair products.
Bath towel and hand towels
Torch (in case of power cuts).
Umbrella - it might rain anytime. 
The Unexciting stuff
Stationery
Diary or files
Electronics – An extension lead, as you may not have many plugs in your room. Don’t forget any chargers for your phone, laptop, Kindle, or any other electrics you plan to bring. Take USB cable, WiFi box (if required) or a hard disk/pendrive with you.
Important documents – Passport, photos, National Insurance card, medical insurance, PAN card, Adhaar card or passport. Remember any confirmation letters of your scholarship or bursary, if appropriate, or acceptance letters from your university.
Credit/Debit cards.
The Cool Stuff
A small bag for going away on weekends.
Formal/informal clothes for various events.
Photos / Wall posters
Induction/Kettle (very important, usually at late nights)
Couple of glass/mugs/utensils to cook and eat! 
Digital camera, if you’re into photography. 
Finally, board games and a pack of cards – Armed with these, you’re sure to break-the-ice and pave the way to make some lifelong mates. 😊
